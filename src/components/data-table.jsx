export function DataTable({
  data: initialData = sampleTenders
}) {
  const [data, setData] = React.useState(() => initialData)
  const [rowSelection, setRowSelection] = React.useState({})
  const [columnVisibility, setColumnVisibility] =
    React.useState({})
  const [columnFilters, setColumnFilters] = React.useState([])
  const [sorting, setSorting] = React.useState([])
  const [pagination, setPagination] = React.useState({
    pageIndex: 0,
    pageSize: 10,
  })
  const sortableId = React.useId()
  const sensors = useSensors(
    useSensor(MouseSensor, {}),
    useSensor(TouchSensor, {}),
    useSensor(KeyboardSensor, {})
  )

  const dataIds = React.useMemo(() => data?.map(({ id }) => id) || [], [data])

  const table = useReactTable({
    data,
    columns,
    state: {
      sorting,
      columnVisibility,
      rowSelection,
      columnFilters,
      pagination,
    },
    getRowId: (row) => row.id.toString(),
    enableRowSelection: true,
    onRowSelectionChange: setRowSelection,
    onSortingChange: setSorting,
    onColumnFiltersChange: setColumnFilters,
    onColumnVisibilityChange: setColumnVisibility,
    onPaginationChange: setPagination,
    getCoreRowModel: getCoreRowModel(),
    getFilteredRowModel: getFilteredRowModel(),
    getPaginationRowModel: getPaginationRowModel(),
    getSortedRowModel: getSortedRowModel(),
    getFacetedRowModel: getFacetedRowModel(),
    getFacetedUniqueValues: getFacetedUniqueValues(),
  })

  // New: support tender-shaped data (tenderNo, description, department, province, category, status, closingDate)
  const isTenderShape = React.useMemo(() => {
    return Array.isArray(data) && data.length > 0 && (data[0].tenderNo !== undefined || data[0].department !== undefined)
  }, [data])

  const [query, setQuery] = React.useState("")
  const [pageIndexLocal, setPageIndexLocal] = React.useState(0)
  const [pageSizeLocal, setPageSizeLocal] = React.useState(10)

  const filteredData = React.useMemo(() => {
    if (!query) return data || []
    const q = query.toLowerCase()
    return (data || []).filter((r) => {
      if (isTenderShape) {
        return (
          (r.tenderNo && r.tenderNo.toLowerCase().includes(q)) ||
          (r.description && r.description.toLowerCase().includes(q)) ||
          (r.department && r.department.toLowerCase().includes(q)) ||
          (r.province && r.province.toLowerCase().includes(q)) ||
          (r.category && r.category.toLowerCase().includes(q))
        )
      }
      // fallback: search common keys
      return (
        (r.header && String(r.header).toLowerCase().includes(q)) ||
        (r.type && String(r.type).toLowerCase().includes(q)) ||
        (r.reviewer && String(r.reviewer).toLowerCase().includes(q))
      )
    })
  }, [data, query, isTenderShape])

  React.useEffect(() => {
    setPageIndexLocal(0)
  }, [query, pageSizeLocal, filteredData.length])

  const totalRows = filteredData.length
  const totalPages = Math.max(1, Math.ceil(totalRows / pageSizeLocal))
  const paged = filteredData.slice(pageIndexLocal * pageSizeLocal, (pageIndexLocal + 1) * pageSizeLocal)

  function handleDragEnd(event) {
    const { active, over } = event
    if (active && over && active.id !== over.id) {
      setData((data) => {
        const oldIndex = dataIds.indexOf(active.id)
        const newIndex = dataIds.indexOf(over.id)
        return arrayMove(data, oldIndex, newIndex);
      })
    }
  }

  return (
    <Tabs defaultValue="outline" className="w-full flex-col justify-start gap-6">
      <div className="flex items-center justify-between px-4 lg:px-6">
        <Label htmlFor="view-selector" className="sr-only">
          View
        </Label>
        <Select defaultValue="outline">
          <SelectTrigger className="flex w-fit @4xl/main:hidden" size="sm" id="view-selector">
            <SelectValue placeholder="Select a view" />
          </SelectTrigger>
          <SelectContent>
            <SelectItem value="outline">All</SelectItem>
            <SelectItem value="past-performance"> Performance</SelectItem>
            <SelectItem value="key-personnel">Favorites</SelectItem>
            <SelectItem value="focus-documents">Draft Documents</SelectItem>
          </SelectContent>
        </Select>
        <TabsList
          className="**:data-[slot=badge]:bg-muted-foreground/30 hidden **:data-[slot=badge]:size-5 **:data-[slot=badge]:rounded-full **:data-[slot=badge]:px-1 @4xl/main:flex">
          <TabsTrigger value="outline">All </TabsTrigger>
          <TabsTrigger value="past-performance">
            Performance <Badge variant="secondary">3</Badge>
          </TabsTrigger>
          <TabsTrigger value="key-personnel">
            Favorites <Badge variant="secondary">2</Badge>
          </TabsTrigger>
          <TabsTrigger value="focus-documents">Draft Documents</TabsTrigger>
        </TabsList>
        <div className="flex items-center gap-2">
          <DropdownMenu>
            <DropdownMenuTrigger asChild>
              <Button variant="outline" size="sm">
                <IconLayoutColumns />
                <span className="hidden lg:inline">Customize Columns</span>
                <span className="lg:hidden">Columns</span>
                <IconChevronDown />
              </Button>
            </DropdownMenuTrigger>
            <DropdownMenuContent align="end" className="w-56">
              {table
                .getAllColumns()
                .filter((column) =>
                typeof column.accessorFn !== "undefined" &&
                column.getCanHide())
                .map((column) => {
                  return (
                    <DropdownMenuCheckboxItem
                      key={column.id}
                      className="capitalize"
                      checked={column.getIsVisible()}
                      onCheckedChange={(value) =>
                        column.toggleVisibility(!!value)
                      }>
                      {column.id}
                    </DropdownMenuCheckboxItem>
                  );
                })}
            </DropdownMenuContent>
          </DropdownMenu>
          <Button variant="outline" size="sm">
            <IconPlus />
            <span className="hidden lg:inline">Add Section</span>
          </Button>
        </div>
      </div>
      <TabsContent value="outline" className="relative flex flex-col gap-4 overflow-auto px-4 lg:px-6">
        <div className="flex items-center justify-between gap-4">
          <Input placeholder="Search tenders, departments, or keywords..." className="flex-1" value={query} onChange={(e) => setQuery(e.target.value)} />
          <div className="flex items-center gap-2">
            <Select value={`${pageSizeLocal}`} onValueChange={(v) => setPageSizeLocal(Number(v))}>
              <SelectTrigger size="sm" className="w-24">
                <SelectValue placeholder={`${pageSizeLocal}`} />
              </SelectTrigger>
              <SelectContent side="top">
                {[5,10,20,30].map((s) => <SelectItem key={s} value={`${s}`}>{s} per page</SelectItem>)}
              </SelectContent>
            </Select>
          </div>
        </div>

        <div className="overflow-hidden rounded-lg border">
          {/* Desktop: table */}
          <div className="hidden md:block">
            <Table>
              <TableHeader className="bg-muted sticky top-0 z-10">
                <TableRow>
                  <TableHead>Tender No.</TableHead>
                  <TableHead>Description</TableHead>
                  <TableHead>Department</TableHead>
                  <TableHead>Province</TableHead>
                  <TableHead>Category</TableHead>
                  <TableHead>Status</TableHead>
                  <TableHead>Closing Date</TableHead>
                </TableRow>
              </TableHeader>
              <TableBody>
                {paged.map((row) => (
                  <TableRow key={row.id} className="hover:bg-muted/50">
                    <TableCell className="font-medium">{row.tenderNo || row.id}</TableCell>
                    <TableCell className="max-w-[420px] truncate">{row.description || row.header}</TableCell>
                    <TableCell>{row.department || row.type}</TableCell>
                    <TableCell>
                      <span className="inline-block rounded-full bg-muted/50 px-3 py-1 text-sm">{row.province || ''}</span>
                    </TableCell>
                    <TableCell>
                      <Badge variant="outline" className="text-muted-foreground">{row.category || row.type}</Badge>
                    </TableCell>
                    <TableCell>
                      <Badge variant={row.status === 'OPEN' || row.status === 'Done' ? 'secondary' : 'default'} className={row.status === 'OPEN' ? 'text-green-600' : ''}>{row.status}</Badge>
                    </TableCell>
                    <TableCell className="whitespace-nowrap">{row.closingDate || row.target || ''}</TableCell>
                  </TableRow>
                ))}
              </TableBody>
            </Table>
          </div>

          {/* Mobile: stacked cards */}
          <div className="md:hidden p-3 space-y-3">
            {paged.map((row) => (
              <div key={row.id} className="rounded-md border p-3 bg-background">
                <div className="flex items-start justify-between">
                  <div>
                    <div className="text-sm font-semibold">{row.tenderNo || row.header}</div>
                    <div className="text-xs text-muted-foreground">{(row.description || row.header || '').slice(0, 120)}</div>
                  </div>
                  <div className="text-sm"><Badge variant="outline">{row.status}</Badge></div>
                </div>
                <div className="mt-2 grid grid-cols-2 gap-2 text-xs">
                  <div><span className="text-muted-foreground">Department</span><div>{row.department || row.type}</div></div>
                  <div><span className="text-muted-foreground">Province</span><div>{row.province}</div></div>
                </div>
              </div>
            ))}
          </div>
        </div>

        <div className="flex items-center justify-between px-2">
          <div className="text-sm text-muted-foreground">Showing {(pageIndexLocal*pageSizeLocal)+1} - {Math.min((pageIndexLocal+1)*pageSizeLocal, totalRows)} of {totalRows}</div>
          <div className="flex items-center gap-2">
            <Button variant="outline" size="sm" onClick={() => setPageIndexLocal(0)} disabled={pageIndexLocal === 0}>First</Button>
            <Button variant="outline" size="sm" onClick={() => setPageIndexLocal((p) => Math.max(0, p-1))} disabled={pageIndexLocal === 0}>Prev</Button>
            <div className="px-2">{pageIndexLocal+1} / {totalPages}</div>
            <Button variant="outline" size="sm" onClick={() => setPageIndexLocal((p) => Math.min(totalPages-1, p+1))} disabled={pageIndexLocal >= totalPages-1}>Next</Button>
            <Button variant="outline" size="sm" onClick={() => setPageIndexLocal(totalPages-1)} disabled={pageIndexLocal >= totalPages-1}>Last</Button>
          </div>
        </div>
      </TabsContent>
      <TabsContent value="past-performance" className="flex flex-col px-4 lg:px-6">
        <div className="aspect-video w-full flex-1 rounded-lg border border-dashed"></div>
      </TabsContent>
      <TabsContent value="key-personnel" className="flex flex-col px-4 lg:px-6">
        <div className="aspect-video w-full flex-1 rounded-lg border border-dashed"></div>
      </TabsContent>
      <TabsContent value="focus-documents" className="flex flex-col px-4 lg:px-6">
        <div className="aspect-video w-full flex-1 rounded-lg border border-dashed"></div>
      </TabsContent>
    </Tabs>
  );
}

